import sys
sys.stdin = open("bj2193input.txt")
dp = [[] for _ in range(91)]
# dp = [0, 1, 1, 2, 3, 4, 6, 10
dp[1] = [0, 1]
dp[2] = [1, 0]
dp[3] = [1, 1]
dp[4] = [2, 1]
for i in range(5, 91):
    i_0, i_1 = dp[i-1]
    dp[i] = [i_0+i_1, i_0]

N = int(input())
print(sum(dp[N]))

